# String Slicing and Indexing in Python

# Input string
text = "PYTHON PROGRAMMING"

# Indexing examples
print("Original String:", text)
print("First character:", text[0])       
print("Last character:", text[-1])      
print("Fifth character:", text[4])       

# Slicing examples
print("Substring (0 to 6):", text[0:6])  
print("Substring (7 to end):", text[7:]) 
print("Substring (start to 6):", text[:6])
print("Substring with step 2:", text[::2]) 
print("Reverse string:", text[::-1])     
